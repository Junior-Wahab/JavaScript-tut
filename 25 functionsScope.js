// function
// Expression
function sayHi() {
  // function body
  console.log('Hello World');
}
// invoking a function
sayHi;

// defining / declaring a function
function add(num1, num2) {
  console.log(num1 + num2);
}

add(5, 10);

// return a value
function subtract(num1, num2) {
  return num1 - num2;
}

const result = subtract(10, 5);
console.log(result);
